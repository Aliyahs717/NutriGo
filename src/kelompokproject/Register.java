        String firstname, lastname, username, password, query;
        String SUrl, SUser, SPass;
        SUrl = "jdbc:MySQL://localhost:3306/nutrigo";
        SUser = "root";
        SPass = "";
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
            Statement st = con.createStatement();
            if("".equals(fname.getText())){
                JOptionPane.showMessageDialog(new JFrame(), "Full Name is require", "Error",
                        JOptionPane.ERROR_MESSAGE);
            }else if("".equals(lname.getText())){
                JOptionPane.showMessageDialog(new JFrame(), "Last name is require", "Error",
                        JOptionPane.ERROR_MESSAGE);
            }else if("".equals(uname.getText())){
                JOptionPane.showMessageDialog(new JFrame(), "Username is require", "Error",
                        JOptionPane.ERROR_MESSAGE);
            }else if("".equals(passw.getText())){
                JOptionPane.showMessageDialog(new JFrame(), "Password is require", "Error",
                        JOptionPane.ERROR_MESSAGE);
            }else {
                
            firstname = fname.getText(); 
            lastname   = lname.getText();
            username = uname.getText();
            password = passw.getText();
            System.out.println(passw);
            
            query = "INSERT INTO registnutrigo(first_name, last_name, username, password)"+
                    "VALUES('"+firstname+"', '"+lastname+"' , '"+username+"', '"+password+"')";
            
            st.execute(query);
            fname.setText("");
            lname.setText("");
            uname.setText("");
            passw.setText("");
            showMessageDialog(null, "New account has been created successfully!");
            }
        }catch(Exception e){
           System.out.println("Error!" + e.getMessage()); 
        }
